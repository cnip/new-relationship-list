$Import('kit.io.inter');
STK.register('common.trans.validateCode',function($){
	var t = $.kit.io.inter();
	var g = t.register;
	g('checkValidate',{'url':'/aj/pincode/verified', 'method':'post'});
	/*
	 {secode : 验证码}
	 */
	return t;
});
