$Import("kit.io.inter");

STK.register('common.trans.vote',function($){
	
		var t = $.kit.io.inter();
		var g = t.register;
		g('add',		{'url':'/aj/vote/add', 'method': 'post'});   //添加投票
		g('join',		{'url':'/aj/vote/join', 'method': 'post'});	// url OK
		return t;
});
